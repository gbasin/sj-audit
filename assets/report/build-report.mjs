#!/usr/bin/env node
// build-report.mjs — assemble the self-contained audit report from a run directory.
//
// Usage:
//   node build-report.mjs --run <run-dir> [--out <file>] [--template <file>]
//                         [--app <name>] [--title <title>] [--generated <ts>]
//
// Reads whatever exists in <run-dir>:
//   results.json     { analyses:[...], synthesis:{...} }  (synthesis.json overrides if present)
//   synthesis.json   {...}                                 (optional standalone)
//   journey.json     [ {img,title,blurb,verdict,observation,tags,reverse,num} ]
//   mockups.json     [ {title,subtitle,frames:[{label,tag,html|img,caption,live}]} ]
//   metrics.json     { surfaces:[ {name,viewport,theme,lighthouse,axe,contrast} ] }
//   coverage.json    { tested:[], missed:[], notes }       (or synthesis.coverage)
//   tokens.json|css  app design tokens for on-brand mockups
//   meta.json        { appName, appKey, lenses, mode, eyebrow, subtitle, footer, ... } overrides
//
// Writes a single self-contained index.html.

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

function parseArgs(argv) {
  const o = {}
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i]
    if (a.startsWith('--')) { const k = a.slice(2); const v = (argv[i + 1] && !argv[i + 1].startsWith('--')) ? argv[++i] : 'true'; o[k] = v }
  }
  return o
}
const args = parseArgs(process.argv)
if (!args.run) { console.error('error: --run <run-dir> is required'); process.exit(2) }
const RUN = path.resolve(args.run)
const OUT = path.resolve(args.out || path.join(RUN, 'index.html'))
const TEMPLATE = path.resolve(args.template || path.join(__dirname, 'template.html'))

function readJSON(p) { try { return JSON.parse(fs.readFileSync(p, 'utf8')) } catch (e) { return null } }
function exists(p) { try { return fs.existsSync(p) } catch (e) { return false } }
const rj = (name) => readJSON(path.join(RUN, name))

// ---- load + normalize ----
const results = rj('results.json') || {}
let analyses = Array.isArray(results) ? results : (results.analyses || [])
let synthesis = rj('synthesis.json') || results.synthesis || {}
const journey = rj('journey.json') || []
const mockups = rj('mockups.json') || []
const metrics = rj('metrics.json') || null
const coverage = rj('coverage.json') || null
if (coverage && !synthesis.coverage) synthesis.coverage = coverage
const metaIn = rj('meta.json') || {}

// derive lenses from the data if not provided
function deriveLenses() {
  const set = new Set()
  for (const an of analyses) for (const l of (an.lensesApplied || [])) set.add(l)
  for (const an of analyses) for (const f of (an.findings || [])) if (f.lens) set.add(f.lens)
  return [...set]
}

let generated = args.generated || metaIn.generatedAt
if (!generated) {
  // prefer the run dir's name if it looks like a timestamp, else the results mtime, else now
  const base = path.basename(RUN)
  if (/^\d{8}/.test(base)) generated = base
  else { try { generated = new Date(fs.statSync(path.join(RUN, 'results.json')).mtime).toISOString().replace('T', ' ').slice(0, 16) } catch (e) { generated = new Date().toISOString().replace('T', ' ').slice(0, 16) } }
}

const appName = args.app || metaIn.appName || 'App'
const lenses = metaIn.lenses || deriveLenses()
const meta = {
  appName,
  appKey: metaIn.appKey || appName.toLowerCase().replace(/[^a-z0-9]+/g, '-'),
  title: args.title || metaIn.title || `${appName} — First-Run UX/UI Audit`,
  eyebrow: metaIn.eyebrow || 'First-run UX/UI audit',
  subtitle: metaIn.subtitle || synthesis.northStar || '',
  lenses,
  mode: metaIn.mode || '',
  generatedAt: generated,
  footer: metaIn.footer || `Generated by sj-audit · ${generated}. Every finding is grounded in a screenshot or a file:line; findings are adversarially verified before shipping.`,
}

const DATA = { meta, analyses, synthesis, journey, mockups, metrics }

// ---- tokens ----
function tokensCss() {
  // App tokens go to BOTH :root (default/dark report chrome -> on-brand) AND .frame
  // (so mockups stay pinned to the app's real tokens even when the report chrome is toggled light).
  let body = ''
  const cssPath = path.join(RUN, 'tokens.css')
  if (exists(cssPath)) {
    body = fs.readFileSync(cssPath, 'utf8').replace(/<\/style>/gi, '')
    // If the file is a bare :root{...} block, also pin it onto .frame.
    const m = body.match(/:root\s*\{([\s\S]*?)\}/)
    if (m) body += `\n.frame{${m[1]}}`
    return body
  }
  const tj = rj('tokens.json')
  if (tj && typeof tj === 'object') {
    const vars = Object.entries(tj).map(([k, v]) => `  ${k.startsWith('--') ? k : '--' + k}:${v};`).join('\n')
    return `:root{\n${vars}\n}\n.frame{\n${vars}\n}`
  }
  return '' // fall back to template defaults (dark)
}

// ---- inject ----
let html = fs.readFileSync(TEMPLATE, 'utf8')
const json = JSON.stringify(DATA).replace(/</g, '\\u003c')
const tokens = tokensCss()
const title = meta.title.replace(/</g, '&lt;')
html = html.replaceAll('__DATA__', () => json)
html = html.replaceAll('__TOKENS__', () => tokens)
html = html.replaceAll('__TITLE__', () => title)

const leftover = (html.match(/__DATA__|__TOKENS__|__TITLE__/g) || []).length
fs.mkdirSync(path.dirname(OUT), { recursive: true })
fs.writeFileSync(OUT, html)

const nFindings = analyses.reduce((a, an) => a + (an.findings || []).length, 0)
const nStories = analyses.reduce((a, an) => a + (an.stories || []).length, 0)
console.log(`wrote ${OUT} (${(fs.statSync(OUT).size / 1024).toFixed(0)} KB)`)
console.log(`  app=${appName} lenses=[${lenses.join(', ')}] generated=${generated}`)
console.log(`  ${analyses.length} areas · ${nFindings} findings · ${nStories} stories · ${(synthesis.topIssues || []).length} ranked issues · ${mockups.length} mockups · journey=${journey.length} · metrics=${metrics ? (metrics.surfaces || []).length + ' surfaces' : 'none'}`)
if (leftover) { console.error(`WARNING: ${leftover} placeholder(s) left unreplaced`); process.exit(1) }
